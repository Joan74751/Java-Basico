public class OperadoresRelacionales {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       int a = 10;
       int b = 5;
       
        System.out.println("a = b: " + (a == b) );
        System.out.println("a < b: " + (a < b));
        System.out.println("a <= b: " + (a <= b));
        System.out.println("a > b: " + (a > b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a != b: " + (a != b));

    }
    
}
